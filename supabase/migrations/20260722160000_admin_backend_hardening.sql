create or replace function public.admin_moderate_posts_v1(
  p_post_ids uuid[],
  p_status text,
  p_reason text,
  p_admin_id uuid
)
returns setof public.posts
language plpgsql security definer set search_path = public, pg_temp as $$
begin
  if p_status not in ('published', 'hidden') then raise exception 'ADMIN_INVALID_POST_STATUS' using errcode = '22023'; end if;
  if cardinality(p_post_ids) = 1 then
    if not exists (select 1 from public.posts where id = p_post_ids[1]) then
      raise exception 'ADMIN_POST_NOT_FOUND' using errcode = 'P0001';
    end if;
    if exists (select 1 from public.posts where id = p_post_ids[1] and status = 'deleted') then
      raise exception 'ADMIN_POST_DELETED' using errcode = 'P0001';
    end if;
  end if;
  if p_status = 'hidden' then
    update public.community_post_pins set status = 'inactive'
    where post_id = any(p_post_ids) and status = 'active';
  end if;
  return query update public.posts set
    status = p_status, moderated_by = p_admin_id, moderated_at = now(),
    moderation_reason = case when p_status = 'hidden' then coalesce(nullif(btrim(p_reason), ''), 'Hidden by admin') else null end
  where id = any(p_post_ids) and status <> 'deleted'
  returning *;
end;
$$;

create or replace function public.admin_resolve_moderation_report_v1(
  p_report_id uuid,
  p_status text,
  p_resolution_note text,
  p_hide_content boolean,
  p_mute_user boolean,
  p_muted_until timestamptz,
  p_muted_reason text,
  p_admin_id uuid
)
returns public.community_reports
language plpgsql security definer set search_path = public, pg_temp as $$
declare
  report_row public.community_reports%rowtype;
begin
  select * into report_row from public.community_reports where id = p_report_id for update;
  if report_row.id is null then raise exception 'ADMIN_REPORT_NOT_FOUND' using errcode = 'P0001'; end if;
  if report_row.status not in ('open', 'reviewed') then raise exception 'ADMIN_REPORT_ALREADY_REVIEWED' using errcode = 'P0001'; end if;
  if p_status not in ('reviewed', 'resolved', 'rejected') then raise exception 'ADMIN_INVALID_REPORT_STATUS' using errcode = '22023'; end if;
  if p_hide_content then
    if report_row.target_type = 'post' and report_row.post_id is not null then
      update public.community_post_pins set status = 'inactive' where post_id = report_row.post_id and status = 'active';
      update public.posts set status = 'hidden', moderated_by = p_admin_id, moderated_at = now(), moderation_reason = p_resolution_note
      where id = report_row.post_id and status <> 'deleted';
    elsif report_row.target_type = 'comment' and report_row.comment_id is not null then
      update public.comments set status = 'hidden', moderated_by = p_admin_id, moderated_at = now(), moderation_reason = p_resolution_note
      where id = report_row.comment_id and status <> 'deleted';
    end if;
  end if;
  if p_mute_user and report_row.reported_user_id is not null then
    update public.profiles set muted_until = coalesce(p_muted_until, now() + interval '1 year'),
      muted_reason = coalesce(nullif(btrim(p_muted_reason), ''), 'Community report upheld'),
      muted_by = p_admin_id, muted_at = now()
    where id = report_row.reported_user_id;
  end if;
  update public.community_reports set status = p_status, resolved_by = p_admin_id,
    resolved_at = now(), resolution_note = p_resolution_note
  where id = report_row.id returning * into report_row;
  return report_row;
end;
$$;

create or replace function public.admin_revoke_sessions_on_account_security_change()
returns trigger language plpgsql security definer set search_path = public, pg_temp as $$
begin
  if old.active is distinct from new.active
    or old.role is distinct from new.role
    or old.password_hash is distinct from new.password_hash then
    update public.admin_sessions set revoked_at = coalesce(revoked_at, now())
    where admin_id = new.id and revoked_at is null;
  end if;
  return new;
end;
$$;
