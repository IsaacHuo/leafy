create or replace function public.admin_approve_campus_request_v1(
  p_request_id uuid,
  p_admin_id uuid,
  p_campus_id text default null,
  p_display_name text default null,
  p_short_name text default null,
  p_new_campus_id text default null,
  p_admin_note text default null
)
returns public.campus_membership_requests
language plpgsql security definer set search_path = public, pg_temp as $$
declare
  request_row public.campus_membership_requests%rowtype;
  target_campus_id text;
  target_display_name text;
  normalized_name text;
begin
  select * into request_row from public.campus_membership_requests
  where id = p_request_id for update;
  if request_row.id is null then raise exception 'COMMUNITY_REQUEST_NOT_FOUND' using errcode = 'P0001'; end if;
  if request_row.status <> 'pending' then raise exception 'ADMIN_CAMPUS_REQUEST_ALREADY_REVIEWED' using errcode = 'P0001'; end if;

  if request_row.request_type = 'school_change' then
    target_campus_id := request_row.requested_campus_id;
  elsif nullif(lower(btrim(coalesce(p_campus_id, ''))), '') is not null
    and lower(btrim(p_campus_id)) <> 'new' then
    target_campus_id := lower(btrim(p_campus_id));
  else
    target_display_name := coalesce(nullif(btrim(p_display_name), ''), request_row.school_name);
    normalized_name := public.normalize_school_name(target_display_name);
    if normalized_name in (public.normalize_school_name('北京林业大学'), public.normalize_school_name('北林')) then
      raise exception 'ADMIN_INVALID_CAMPUS_TARGET' using errcode = '22023';
    end if;
    select id into target_campus_id from public.campuses where normalized_name = public.normalize_school_name(target_display_name);
    if target_campus_id is null then
      target_campus_id := lower(btrim(coalesce(nullif(p_new_campus_id, ''), 'campus-' || replace(request_row.id::text, '-', ''))));
      insert into public.campuses(id, display_name, short_name, connector_kind, status, normalized_name, is_community_enabled, is_system)
      values (target_campus_id, target_display_name, coalesce(nullif(btrim(p_short_name), ''), left(target_display_name, 6)),
        'custom', 'active', normalized_name, true, false);
    end if;
  end if;
  if target_campus_id is null or target_campus_id = 'general'
    or (request_row.request_type <> 'school_change' and target_campus_id = 'bjfu') then
    raise exception 'ADMIN_INVALID_CAMPUS_TARGET' using errcode = '22023';
  end if;
  if not exists (select 1 from public.campuses where id = target_campus_id and status = 'active' and is_community_enabled) then
    raise exception 'COMMUNITY_CAMPUS_NOT_FOUND' using errcode = 'P0001';
  end if;
  return public.approve_campus_membership_request(request_row.id, target_campus_id, p_admin_id, p_admin_note);
end;
$$;

