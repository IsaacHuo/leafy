create or replace function public.refresh_dish_rating_summary(target_dish_id bigint)
returns void language sql security definer set search_path = public, pg_temp as $$
  update public.dish_catalog set
    rating_average = coalesce((select round(avg(stars)::numeric, 1) from public.dish_ratings where dish_id = target_dish_id), 0),
    rating_count = (select count(*)::integer from public.dish_ratings where dish_id = target_dish_id),
    rating_1_count = (select count(*)::integer from public.dish_ratings where dish_id = target_dish_id and stars = 1),
    rating_2_count = (select count(*)::integer from public.dish_ratings where dish_id = target_dish_id and stars = 2),
    rating_3_count = (select count(*)::integer from public.dish_ratings where dish_id = target_dish_id and stars = 3),
    rating_4_count = (select count(*)::integer from public.dish_ratings where dish_id = target_dish_id and stars = 4),
    rating_5_count = (select count(*)::integer from public.dish_ratings where dish_id = target_dish_id and stars = 5)
  where id = target_dish_id;
$$;

create or replace function public.handle_dish_rating_summary_sync()
returns trigger language plpgsql security definer set search_path = public, pg_temp as $$
begin
  if tg_op = 'DELETE' then
    perform public.refresh_dish_rating_summary(old.dish_id);
    return old;
  end if;
  if tg_op = 'UPDATE' and old.dish_id <> new.dish_id then
    perform public.refresh_dish_rating_summary(old.dish_id);
  end if;
  perform public.refresh_dish_rating_summary(new.dish_id);
  return new;
end;
$$;

