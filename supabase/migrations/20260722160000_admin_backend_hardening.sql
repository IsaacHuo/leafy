create or replace function public.admin_approve_catalog_suggestion_v1(
  p_suggestion_id uuid,
  p_admin_id uuid,
  p_admin_note text default null
)
returns public.catalog_suggestions
language plpgsql security definer set search_path = public, pg_temp as $$
declare
  suggestion public.catalog_suggestions%rowtype;
  target_teacher_id bigint;
  target_course_id bigint;
  target_dish_id bigint;
begin
  select * into suggestion from public.catalog_suggestions
  where id = p_suggestion_id for update;
  if suggestion.id is null then raise exception 'ADMIN_CATALOG_SUGGESTION_NOT_FOUND' using errcode = 'P0001'; end if;
  if suggestion.status <> 'open' then raise exception 'ADMIN_CATALOG_SUGGESTION_ALREADY_REVIEWED' using errcode = 'P0001'; end if;
  if nullif(btrim(suggestion.campus_id), '') is null then raise exception 'ADMIN_CAMPUS_REQUIRED' using errcode = '22023'; end if;
  if suggestion.user_id is not null and not exists (
    select 1 from public.profiles where id = suggestion.user_id
      and community_campus_id = suggestion.campus_id and community_access_status = 'approved'
  ) then
    raise exception 'ADMIN_CATALOG_SUGGESTION_CAMPUS_MISMATCH' using errcode = 'P0001';
  end if;

  if suggestion.suggestion_type = 'teacher' then
    select id into target_teacher_id from public.teachers
    where campus_id = suggestion.campus_id and lower(btrim(name)) = lower(btrim(suggestion.name))
      and lower(btrim(unit)) = lower(btrim(suggestion.unit))
    order by id limit 1 for update;
    if target_teacher_id is null then
      insert into public.teachers(campus_id, name, unit, status)
      values (suggestion.campus_id, btrim(suggestion.name), btrim(suggestion.unit), 'published')
      on conflict do nothing returning id into target_teacher_id;
      if target_teacher_id is null then
        select id into target_teacher_id from public.teachers
        where campus_id = suggestion.campus_id and lower(btrim(name)) = lower(btrim(suggestion.name))
          and lower(btrim(unit)) = lower(btrim(suggestion.unit)) order by id limit 1 for update;
      end if;
    end if;
    update public.teachers set status = 'published' where id = target_teacher_id and status <> 'published';
    if suggestion.initial_stars is not null and suggestion.user_id is not null then
      insert into public.teacher_ratings(teacher_id, user_id, stars)
      values (target_teacher_id, suggestion.user_id, suggestion.initial_stars)
      on conflict (teacher_id, user_id) do update set stars = excluded.stars;
    end if;
  elsif suggestion.suggestion_type = 'course' then
    select id into target_course_id from public.course_catalog
    where campus_id = suggestion.campus_id and lower(btrim(name)) = lower(btrim(suggestion.name))
      and lower(btrim(unit)) = lower(btrim(suggestion.unit))
      and lower(btrim(category)) = lower(btrim(coalesce(suggestion.category, '公选课')))
    order by id limit 1 for update;
    if target_course_id is null then
      insert into public.course_catalog(campus_id, name, unit, category, credit, status)
      values (suggestion.campus_id, btrim(suggestion.name), btrim(suggestion.unit),
        coalesce(nullif(btrim(suggestion.category), ''), '公选课'), coalesce(suggestion.credit, 0), 'published')
      on conflict do nothing returning id into target_course_id;
      if target_course_id is null then
        select id into target_course_id from public.course_catalog
        where campus_id = suggestion.campus_id and lower(btrim(name)) = lower(btrim(suggestion.name))
          and lower(btrim(unit)) = lower(btrim(suggestion.unit))
          and lower(btrim(category)) = lower(btrim(coalesce(suggestion.category, '公选课')))
        order by id limit 1 for update;
      end if;
    end if;
    update public.course_catalog set status = 'published', credit = coalesce(suggestion.credit, credit)
    where id = target_course_id and (status <> 'published' or suggestion.credit is not null);
    if suggestion.initial_stars is not null and suggestion.user_id is not null then
      insert into public.course_ratings(course_id, user_id, stars)
      values (target_course_id, suggestion.user_id, suggestion.initial_stars)
      on conflict (course_id, user_id) do update set stars = excluded.stars;
    end if;
  elsif suggestion.suggestion_type = 'dish' then
    select id into target_dish_id from public.dish_catalog
    where campus_id = suggestion.campus_id and lower(btrim(name)) = lower(btrim(suggestion.name))
      and lower(btrim(location)) = lower(btrim(suggestion.unit))
    order by id limit 1 for update;
    if target_dish_id is null then
      insert into public.dish_catalog(campus_id, name, location, status)
      values (suggestion.campus_id, btrim(suggestion.name), btrim(suggestion.unit), 'published')
      on conflict do nothing returning id into target_dish_id;
      if target_dish_id is null then
        select id into target_dish_id from public.dish_catalog
        where campus_id = suggestion.campus_id and lower(btrim(name)) = lower(btrim(suggestion.name))
          and lower(btrim(location)) = lower(btrim(suggestion.unit)) order by id limit 1 for update;
      end if;
    end if;
    update public.dish_catalog set status = 'published' where id = target_dish_id and status <> 'published';
    if suggestion.initial_stars is not null and suggestion.user_id is not null then
      insert into public.dish_ratings(dish_id, user_id, stars)
      values (target_dish_id, suggestion.user_id, suggestion.initial_stars)
      on conflict (dish_id, user_id) do update set stars = excluded.stars;
    end if;
  else
    raise exception 'ADMIN_INVALID_CATALOG_SUGGESTION_TYPE' using errcode = '22023';
  end if;

  update public.catalog_suggestions set
    status = 'approved', approved_teacher_id = target_teacher_id,
    approved_course_id = target_course_id, approved_dish_id = target_dish_id,
    admin_note = nullif(btrim(coalesce(p_admin_note, '')), ''),
    reviewed_by = p_admin_id, reviewed_at = now()
  where id = suggestion.id returning * into suggestion;
  return suggestion;
end;
$$;

