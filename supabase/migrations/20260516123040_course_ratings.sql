create table if not exists public.course_catalog (
  id bigint generated by default as identity primary key,
  name text not null,
  unit text not null,
  category text not null default '公选课',
  credit numeric(4, 1) not null default 0,
  status text not null default 'published',
  search_text text generated always as (
    lower(
      coalesce(name, '') || ' ' ||
      coalesce(unit, '') || ' ' ||
      coalesce(category, '')
    )
  ) stored,
  rating_average numeric(2, 1) not null default 0,
  rating_count integer not null default 0,
  rating_1_count integer not null default 0,
  rating_2_count integer not null default 0,
  rating_3_count integer not null default 0,
  rating_4_count integer not null default 0,
  rating_5_count integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint course_catalog_name_unit_category_unique unique (name, unit, category),
  constraint course_catalog_name_not_blank check (nullif(btrim(name), '') is not null),
  constraint course_catalog_unit_not_blank check (nullif(btrim(unit), '') is not null),
  constraint course_catalog_category_not_blank check (nullif(btrim(category), '') is not null),
  constraint course_catalog_credit_valid check (credit >= 0),
  constraint course_catalog_status_check check (status in ('published', 'hidden'))
);

create table if not exists public.course_ratings (
  course_id bigint not null references public.course_catalog (id) on delete cascade on update cascade,
  user_id uuid not null references public.profiles (id) on delete cascade on update cascade,
  stars integer not null check (stars between 1 and 5),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  primary key (course_id, user_id)
);

create index if not exists idx_course_catalog_rating
on public.course_catalog (rating_average desc, rating_count desc, id asc);

create index if not exists idx_course_catalog_search_text
on public.course_catalog (search_text);

create index if not exists idx_course_catalog_category
on public.course_catalog (category, status, rating_average desc, rating_count desc);

create index if not exists idx_course_ratings_user_id
on public.course_ratings (user_id);

drop trigger if exists course_catalog_set_updated_at on public.course_catalog;
create trigger course_catalog_set_updated_at
before update on public.course_catalog
