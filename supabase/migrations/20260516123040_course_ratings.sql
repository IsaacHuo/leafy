execute function public.set_updated_at();

drop trigger if exists course_ratings_set_updated_at on public.course_ratings;
create trigger course_ratings_set_updated_at
before update on public.course_ratings
for each row
execute function public.set_updated_at();

create or replace function public.refresh_course_rating_summary(target_course_id bigint)
returns void
language sql
security definer
set search_path = public
as $$
  update public.course_catalog
  set
    rating_average = coalesce((
      select round(avg(stars)::numeric, 1)
      from public.course_ratings
      where course_id = target_course_id
    ), 0),
    rating_count = (
      select count(*)::integer
      from public.course_ratings
      where course_id = target_course_id
    ),
    rating_1_count = (
      select count(*)::integer
      from public.course_ratings
      where course_id = target_course_id and stars = 1
    ),
    rating_2_count = (
      select count(*)::integer
      from public.course_ratings
      where course_id = target_course_id and stars = 2
    ),
    rating_3_count = (
      select count(*)::integer
      from public.course_ratings
      where course_id = target_course_id and stars = 3
    ),
    rating_4_count = (
      select count(*)::integer
      from public.course_ratings
      where course_id = target_course_id and stars = 4
    ),
    rating_5_count = (
      select count(*)::integer
      from public.course_ratings
      where course_id = target_course_id and stars = 5
    )
  where id = target_course_id;
$$;

create or replace function public.handle_course_rating_summary_sync()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if tg_op = 'DELETE' then
    perform public.refresh_course_rating_summary(old.course_id);
    return old;
  end if;

  if tg_op = 'UPDATE' and old.course_id <> new.course_id then
    perform public.refresh_course_rating_summary(old.course_id);
  end if;

  perform public.refresh_course_rating_summary(new.course_id);
  return new;
end;
$$;

drop trigger if exists course_ratings_sync_summary on public.course_ratings;
create trigger course_ratings_sync_summary
after insert or update or delete on public.course_ratings
for each row
execute function public.handle_course_rating_summary_sync();

